package variaveis;
public class Main {
    public static void main(String[] args) {
        // int
        int idade;
        idade = 19;
        System.out.println(idade);       
        
        // double
        double preco;
        preco = 10.90;
        System.out.println(preco);
        
        // float
        float valor;
        valor = 10.9f;
        System.out.println(valor);
        // boolean
        boolean verd = true;
        System.out.println(verd);
        
        // char
        char letra;
        letra = 'A';
        System.out.println(letra);
    }
}
